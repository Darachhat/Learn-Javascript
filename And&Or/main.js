// and && or || -----------------

const isFree = true;
const hasMoney = false

if ( isFree && hasMoney ) { // and operator &&
    console.log("We can traveling");
} else {
    console.log("We can't traveling");  
}

if ( isFree || hasMoney ) { //  or operator ||
    console.log("We can traveling");
} else {
    console.log("We can't traveling");  
}