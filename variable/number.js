//   + plus
const plus = 10 + 10 
console.log("This is plus result: " + plus); // 20

//   - minus
const minus = 10 - 5
console.log("This is minus result: " + minus); // 5

//   * multiply
let multiply = 10*10
console.log("This is multiply result: " +multiply); // 100

//   / divide
let divide = 10
divide = 10 / 2
console.log("This is divide result: " + divide); // 5​ 

//   % modulus 
const modulus = 10 % 3
console.log("This is modulus result: " + modulus); // 1 សំណល់

//   ** exponentiation
const exponentiation = 10 ** 2
console.log("This is exponentiation result: " + exponentiation); // 100

