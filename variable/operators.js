// =
let x = 10
x = 50
console.log("This is x = 10 then x = 50 result: " + x);

// +=
let num = 1
// num = num + 1
num += 1
console.log("This is num = 1 then num += 1 result: " + num);

// -=
let y = 10
// y = y - 5
y -= 5
console.log("This is y = 10 then y -= 5 result: " + y);

// *=
let z = 10
// z = z * 2
z *= 2
console.log("This is z = 10 then z *= 2 result: " + z);

// /=
let a = 10
// a = a / 2
a /= 2
console.log("This is a = 10 then a /= 2 result: " + a);

// %=
let b = 50
// b = b % 2
b %= 4
console.log("This is b = 50 then b %= 4 result: " + b);

// **=
// ++
// postincrement
let postincrement = 10
// postincrement = postincrement + 1
postincrement++
console.log("This is postincrement++ of 10 result: " + postincrement); // 11
// preincrement
let preincrement = 10
// preincrement = preincrement + 1
++preincrement
console.log("This is ++preincrement of 10 result: " + preincrement); // 11

// --
// postdecrement
let postdecrement = 10
// postdecrement = postdecrement - 1
postdecrement--
console.log("This is postdecrement-- of 10 result: " + postdecrement); // 9
// predecrement
let predecrement = 10
// predecrement = predecrement - 1
--predecrement
console.log("This is --predecrement of 10 result: " + predecrement); // 9
